package br.edu.ifsp.modelo;

public class Regiao {
	
	private String nome;
	private String abreviacao;
	
	public Regiao() {
		
	}
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getAbreviacao() {
		return abreviacao;
	}
	public void setAbreviacao(String abreviacao) {
		this.abreviacao = abreviacao;
	}
	
	

}
